public static void main (String [] args) {
    Scanner sc = new Scanner (System.in);
    int cases = sc.nextInt ();
    String [] str = new String [cases];
    for (int i = 0;
    i < cases; i ++) {
        str [i] = sc.next ();
    }
}


public static void main (String [] args) {
    Scanner sc = new Scanner (System.in);
    int cases = sc.nextInt ();
    boolean [] boolArr = new boolean [cases];
    for (int i = 0;
    i < cases; i ++) {
        boolArr [i] = sc.nextBoolean ();
    }
}
